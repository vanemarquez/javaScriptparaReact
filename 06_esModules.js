// Función para sumar dos números
export function sumar(a, b) {
    return a + b;
  }
  
// Función para restar dos números
  export function restar(a, b) {
    return a - b;
  }
  
// Función para multiplicar dos números
  export function multiplicar(a, b) {
    return a * b;
  }
 
